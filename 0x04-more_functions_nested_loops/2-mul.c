/**
 * mul - multplies two numbers
 *
 * @a: The number to be multiplied
 *
 * @b: The number to be multiplied
 *
 * res: The result of the multiplication
 *
 * Return: returns the result of the multiplication.
 */
int mul(int a, int b)
{
	int res;

	res = a * b;

	return (res);
}
